    def forward_core(self, x: torch.Tensor):
        B, C, H, W = x.shape  # (B,d_inner,H,W)
        L = H * W
        K = 2
        x_hwwh = torch.stack([x.view(B, -1, L), torch.transpose(x, dim0=2, dim1=3).contiguous().view(B, -1, L)],
                             dim=1).view(B, 2, -1, L)
        # xs = torch.cat([x_hwwh, torch.flip(x_hwwh, dims=[-1])], dim=1)  # (b,k,d_inner,l)
        xs = x_hwwh

        # c:dt_rank+d_state*2  d:d_inner
        x_dbl = torch.einsum("b k d l, k c d -> b k c l", xs.view(B, K, -1, L), self.x_proj_weight)
        dts, Bs, Cs = torch.split(x_dbl, [self.dt_rank, self.d_state, self.d_state], dim=2)
        dts = torch.einsum("b k r l, k d r -> b k d l", dts.view(B, K, -1, L), self.dt_projs_weight)  # (b,k,dt_rank,l)
        xs = xs.float().view(B, -1, L)  # (b,k*d_inner,l)
        dts = dts.contiguous().float().view(B, -1, L)  # (b,k*dt_rank,l)
        Bs = Bs.float().view(B, K, -1, L)  # (b,k*_d_state,l)
        Cs = Cs.float().view(B, K, -1, L)  # (b,k*_d_state,l)
        Ds = self.Ds.float().view(-1)  # (k*d_inner)
        As = -torch.exp(self.A_logs.float()).view(-1, self.d_state)  # (k*d_inner,d_state)
        dt_projs_bias = self.dt_projs_bias.float().view(-1)
        out_y = self.selective_scan(
            xs, dts,
            As, Bs, Cs, Ds, z=None,
            delta_bias=dt_projs_bias,
            delta_softplus=True,
            return_last_state=False,
        ).view(B, K, -1, L)
        assert out_y.dtype == torch.float

        # inv_y = torch.flip(out_y[:, 2:4], dims=[-1]).view(B, 2, -1, L)
        wh_y = torch.transpose(out_y[:, 1].view(B, -1, W, H), dim0=2, dim1=3).contiguous().view(B, -1, L)
        # invwh_y = torch.transpose(inv_y[:, 1].view(B, -1, W, H), dim0=2, dim1=3).contiguous().view(B, -1, L)

        # return out_y[:, 0], inv_y[:, 0], wh_y, invwh_y
        return out_y[:, 0], wh_y
    

    def forward(self, x: torch.Tensor, **kwargs):
        x = rearrange(x, "B C H W -> B H W C")
        B, H, W, C = x.shape
        xz = self.in_proj(x)  # (B,H,W,d_inner*2)
        x, z = xz.chunk(2, dim=-1)
        x = x.permute(0, 3, 1, 2).contiguous()
        x = self.act(self.conv2d(x))  # (B,d_inner,H,W)
        y1, y2 = self.forward_core(x)
        assert y1.dtype == torch.float32
        y = y1 + y2
        y = torch.transpose(y, dim0=1, dim1=2).contiguous().view(B, H, W, -1)
        y = self.out_norm(y)
        y = y * F.silu(z)  # z:(B,H,W,d_inner)
        out = self.out_proj(y)
        if self.dropout is not None:
            out = self.dropout(out)
        return rearrange(out, "B H W D -> B D H W")
    
    
